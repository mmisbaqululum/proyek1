    public void tampilkanData() {
        String[] kolom = {"ID", "Nama", "Username", "Password"};
        ArrayList<Admin> list = new Admin().getAll();
        Object rowData[] = new Object[4];
        
        tbAdmin.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Admin pe : list) {
            rowData[0] = pe.getId_admin();
            rowData[1] = pe.getNama_user();
            rowData[2] = pe.getUsername();
            rowData[3] = pe.getPassword();
            
            ((DefaultTableModel)tbAdmin.getModel()).addRow(rowData);
        }
    }

